//Write a Java program to display the first 10 natural numbers.

package com.corejava.lab_1;

public class natural_no {
	
	    public static void main(String[] args) {
	    	
	        int i = 1;      // Initialize the variable i with the value 1
	        
	        while (i <= 10) {       // Loop while i is less than or equal to 10
	        	
	            System.out.println(i);      // Print the current value of i
	            
	            i++;        // Increment i by 1
	            
	        }
	    }

}

